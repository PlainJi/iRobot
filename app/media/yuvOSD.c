struct yuv_handle *yuvOSD_open(struct yuv_param param)
{
	struct yuv_handle *handle = malloc(sizeof(struct yuv_handle));
	if (!handle)
	{
		printf("--- malloc yuvOSD handle failed\n");
		return NULL;
	}

	CLEAR(*handle);
	handle->params.width = param.width;
	handle->params.height = param.height;
	handle->params.r = param.r;
	handle->params.g = param.g;
	handle->params.b = param.b;
	rgb2yuv(handle->params.r, handle->params.g, handle->params.b, &(handle->params.y), &(handle->params.u), &(handle->params.v));

	return handle;
}

void getShortDateTimeStr(unsigned char *timeTab)
{
	time_t timep;
	struct tm *p_tm;
	timep = time(NULL);
	p_tm = localtime(&timep);
	sprintf((char*)timeTab, "%04d%02d%02d-%02d%02d%02d", 
		p_tm->tm_year+1900, p_tm->tm_mon+1, p_tm->tm_mday, p_tm->tm_hour, p_tm->tm_min, p_tm->tm_sec);
}

void getDateTimeStr(unsigned char *timeTab)
{
	time_t timep;
	struct tm *p_tm;
	timep = time(NULL);
	p_tm = localtime(&timep);
	sprintf((char*)timeTab, "%04d-%02d-%02d %02d:%02d:%02d", 
		p_tm->tm_year+1900, p_tm->tm_mon+1, p_tm->tm_mday, p_tm->tm_hour, p_tm->tm_min, p_tm->tm_sec);
}

void OSDTime_do(struct yuv_handle *yuvHandle, unsigned char *buf)
{
	unsigned char timeTab[20] = {0};
	getDateTimeStr(timeTab);
	LCD_WriteASCIIStr1216(yuvHandle, 20, 20, timeTab, 16, buf);
}



